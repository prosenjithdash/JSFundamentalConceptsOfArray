// Addremove Elements From Arrays Using Push,Pop Shift _ Unshift

//POP
// POP working is last out element
const Y = [25, 48, 392, 994 , 343 , 32, 98];

// Before Element POP
console.log('Before Element POP', Y)

// After Element 01 POP
Y.pop()
console.log('After Element 01 POP', Y)

// After Element 02 POP
Y.pop()
Y.pop()
console.log('After Element 02 POP', Y)

// After Element 03 POP
const out_03_1 = Y.pop()
const out_03_2 = Y.pop()
const out_03_3 = Y.pop()
console.log('After Element 03 POP', Y)
console.log('03 number POP elements: ', out_03_1, out_03_2, out_03_3)