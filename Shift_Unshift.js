// Addremove Elements From Arrays Using Push,Pop Shift _ Unshift

//Shift
// Shift working is fast out element
const z = [95, 38, 92, 94 , 33 , 62, 98];

// Before Element Shift
console.log('Before Element Shift', z)

// After Element shift
z.shift()
console.log('After Element Shift', z)


//Unshift
// Unshift working is fast in element

// After Element shift
z.unshift(100)
console.log('After Element Unshift', z)